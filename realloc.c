/**
 **_memset - fills memory with a constant bytes
 *@s: the pointer to the memory areas
 *@b: the byte to fill *s with
 *@n: the amount of bytes to be fill
 *Return: (s) a pointer to the memory area ss
 */
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < n; i++)
		s[i] = b;
	return (s);
}

/**
 * ffree - frees a string of stringss
 * @pp: string of stringss
 */
void ffree(char **pp)
{
	char **a = pp;

	if (!pp)
		return;
	while (*pp)
		free(*pp++);
	free(a);
}

